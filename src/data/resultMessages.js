// ユーティリティ：ランダム選択
function pick(arr) {
  return arr[Math.floor(Math.random() * arr.length)];
}

export function getResultMessage(power, amount) {
  let amtNum = typeof amount === "number" ? amount : 0;

  // 超高額パターン
  if (amount === "計り知れない生涯年収！") {
    return pick([
      "あなたの眠れる才能が今ここに覚醒！まさに伝説的な金運です。新しい伝説の幕開け…！",
      "財運の星があなたのもとに。これからも大胆に人生を切り開いてください！",
      "運命の歯車が大きく回り始めました。想像を超える財の器です！"
    ]);
  }
  if (amtNum >= 10_000_000_000) {
    if (power >= 11) {
        return pick([
      "あなたの運命はまさに“財の器”。掴んだチャンスを活かして未来を切り拓きましょう！",
      "ここまで来たら迷わず進め！財運の扉が大きく開かれました。",
      "財運と才能の両方を兼ね備えたあなた。新しい目標にもどんどん挑戦を！"
    ]);
    } else {
        return pick([
      "奇跡的な引き寄せ！思い切った一歩がさらなる財運を呼び込みます。",
      "想像を超える金運の流れです。小さな幸運も大切に。",
      "大きな財運に恵まれました。感謝の気持ちも忘れずに！"
    ]);
    }
  }
  if (amtNum < 100_000_000) {
    if (power >= 11) {
        return pick([
      "金額は平均には及ばず控えめですが、あなたの眠れる才能はこれから花開きます。",
      "まだ序章。これからの努力が大きな転機を呼び込みます。",
      "未来には大きな逆転劇が待っています。日々の選択を大切に！"
    ]);
    } else {
        return pick([
      "ここからが逆転劇のスタート。小さなチャレンジが幸運を呼び込みます！",
      "控えめな運勢ですが、一歩一歩の積み重ねが大きな運命を引き寄せます。",
      "今は穏やかな運気。地道な努力が財運アップのカギ！"
    ]);
    }
  }
  if (amtNum < 10_000_000) {
    if (power >= 11) {
        return pick([
      "金額は極端に低いです。このままでは生活保護もしくはホームレス...",
      "まだ間に合う、大丈夫子供からしたら大きい金額だから、ね。",
      "逆にすごい！人生は睡眠と言わんばかりです。飛躍を期待してます"
    ]);
    } else {
        return pick([
      "ここから逆転劇のスタートさせるしかありません。なんでもアドバイスしますよ！",
      "最悪な運勢ですが、一歩一歩の積み重ねが重要です、大きな運命を引き寄せましょう。",
      "今は極端に低い運気。地道な努力を積んで財運アップと人生チェンジを狙うのカギ！"
    ]);
    }
  }
  // 通常パターン
  return pick([
    "今のあなたの金運はこのくらい。平均的な収入で良いのですか？！今後の選択次第でどんどん変化します。もっと前向きに進みましょう！",
    "日々の行動が未来の財運に直結します。コツコツ進んでいきましょう。あなたは並でおさまって良い人間ではありませんよ...!",
    "今の流れを大切に。BIGチャンスは突然やってきます"
  ]);
}